# Sayılar (Numbers): integer, float, complex
# integer: tam sayılar
# float: ondalıklı sayılar
# complex: denklemler, fonksiyonlar

# int örnek
a = 5
# float örnek
b = 10.5
# complex örnek
x = 2j + 1
# Yazdırmak için her seferinde print kullanmamız gerekiyor.
print(a)
print(a * b)
# Değişiklerle birlikte tutmak proje ilerledikçe sağlıklı ilerlememizi sağlar,
# Tipleri değiştirmek
# integer'ı float'a çevirmek için;
print(int(b))
print(float(a))
print(a * b / 10)
# İnteger formda bu şekilde kullanılır.
print(int(a * b / 10))
